const LANG_ATTR = { pt: "pt-BR", en: "en", es: "es", ja: "ja" };
let currentLang = "pt";

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || I18N.pt[key] || key;
}
function specValue(v) {
  if (v.all) return v.all;
  if (v.key) return t(v.key);
  return v[currentLang] || v.pt;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (I18N[lang][key]) el.textContent = I18N[lang][key];
  });
  document.documentElement.setAttribute("lang", LANG_ATTR[lang]);
  document.querySelectorAll(".lang-switch button").forEach(b =>
    b.classList.toggle("active", b.dataset.lang === lang)
  );
  try { localStorage.setItem("paizinho-lang", lang); } catch (e) {}
  const open = currentDetail();
  if (open) renderDetail(open);
}

document.querySelectorAll(".lang-switch button").forEach(btn =>
  btn.addEventListener("click", () => setLang(btn.dataset.lang))
);

