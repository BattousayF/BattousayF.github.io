function currentDetail() {
  const m = location.hash.match(/^#\/(g\d|cava|band|flamenca|classica|colecao)$/);
  if (!m) return null;
  if (m[1] === "colecao") return "colecao";
  return (GUITARS[m[1]] || INSTRUMENTS[m[1]]) ? m[1] : null;
}

function petalsHTML() {
  let out = "";
  for (let i = 0; i < 14; i++) {
    const left = Math.round(Math.random() * 96);
    const dur = (7 + Math.random() * 8).toFixed(1);
    const delay = (Math.random() * 9).toFixed(1);
    const size = (9 + Math.random() * 8).toFixed(0);
    out += `<span class="petal" style="left:${left}%;width:${size}px;height:${Math.round(size*0.85)}px;animation-duration:${dur}s;animation-delay:-${delay}s"></span>`;
  }
  return out;
}

